public class Fraccion
{
    public static double sumar(double x, double y)
    {
        return x + y;
    }

    public static double sumar(double x, int y)
    {
        return x + y;
    }

    public static double restar(double x, double y)
    {
        return x - y;
    }

    public static double restar(double x, int y)
    {
        return x - y;
    }

    public static double multiplicar(double x, double y)
    {
        return x * y;
    }

    public static double multiplicar(double x, int y)
    {
        return x * y;
    }

    public static double dividir(double x, double y)
    {
        return x / y;
    }

    public static double dividir(double x, int y)
    {
        return x / y;
    }

}
